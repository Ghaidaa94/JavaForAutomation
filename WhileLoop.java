package MyFirstJavaPackage;

public class WhileLoop {
    public static void main(String[] args) {
        int count = 10 ;
        while (count>=10)
        {
            System.out.println("count : " + count);
            count--;
        }
        System.out.println();
        System.out.println("Loop Finished");
        System.out.println("Slide 29!!");
    }

}
